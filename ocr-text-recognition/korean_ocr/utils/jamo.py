def compose_unicode(unicode_arr):
    if isinstance(unicode_arr, list) or isinstance(unicode_arr, tuple):
        unicode_arr = np.array(unicode_arr)

    if unicode_arr.ndim == 1:
        unicode_arr = unicode_arr[None]
    texts = []
    for unicode_vec in unicode_arr:
        text = ""
        for vec in unicode_vec:
            if vec != -1:
                text += chr(vec)
        texts.append(text)
    return texts


